-- [EN] Ensure daily partitions for sensor_raw_logs around current day.
-- [ZH] 自動確保 sensor_raw_logs 在當日前後的日分區存在。
CREATE OR REPLACE FUNCTION ensure_sensor_raw_log_partitions(days_back INTEGER DEFAULT 1, days_ahead INTEGER DEFAULT 7)
RETURNS INTEGER
LANGUAGE plpgsql
AS $$
DECLARE
  base_day DATE := timezone('UTC', now())::date;
  offset_idx INTEGER;
  from_date DATE;
  to_date DATE;
  part_name TEXT;
  created_count INTEGER := 0;
BEGIN
  IF days_back < 0 THEN
    days_back := 0;
  END IF;
  IF days_ahead < 0 THEN
    days_ahead := 0;
  END IF;

  FOR offset_idx IN (-days_back)..days_ahead LOOP
    from_date := base_day + offset_idx;
    to_date := from_date + 1;
    part_name := format('sensor_raw_logs_p%s', to_char(from_date, 'YYYYMMDD'));

    IF to_regclass('public.' || part_name) IS NULL THEN
      EXECUTE format(
        'CREATE TABLE %I PARTITION OF sensor_raw_logs FOR VALUES FROM (%L) TO (%L)',
        part_name, from_date, to_date
      );
      created_count := created_count + 1;
    END IF;
  END LOOP;

  RETURN created_count;
END;
$$;

-- [EN] Drop raw-log daily partitions older than retention days.
-- [ZH] 刪除超過保留天數的 raw-log 日分區。
CREATE OR REPLACE FUNCTION prune_sensor_raw_log_partitions(retention_days INTEGER DEFAULT 30)
RETURNS INTEGER
LANGUAGE plpgsql
AS $$
DECLARE
  cutoff_date DATE := timezone('UTC', now())::date - retention_days;
  rel RECORD;
  part_suffix TEXT;
  part_day DATE;
  dropped_count INTEGER := 0;
BEGIN
  IF retention_days < 1 THEN
    retention_days := 1;
    cutoff_date := timezone('UTC', now())::date - retention_days;
  END IF;

  FOR rel IN
    SELECT c.relname AS partition_name
    FROM pg_class c
    JOIN pg_inherits i ON i.inhrelid = c.oid
    JOIN pg_class p ON p.oid = i.inhparent
    WHERE p.relname = 'sensor_raw_logs'
      AND c.relname LIKE 'sensor_raw_logs_p________'
  LOOP
    part_suffix := substring(rel.partition_name FROM 'sensor_raw_logs_p([0-9]{8})');
    IF part_suffix IS NULL THEN
      CONTINUE;
    END IF;

    part_day := to_date(part_suffix, 'YYYYMMDD');
    IF part_day < cutoff_date THEN
      EXECUTE format('DROP TABLE IF EXISTS %I', rel.partition_name);
      dropped_count := dropped_count + 1;
    END IF;
  END LOOP;

  RETURN dropped_count;
END;
$$;

