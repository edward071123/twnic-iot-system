-- [EN] Ensure monthly partitions for sensor_datas around current month.
-- [ZH] 自動確保 sensor_datas 在當前月份前後的月分區存在。
CREATE OR REPLACE FUNCTION ensure_sensor_data_monthly_partitions(months_back INTEGER DEFAULT 1, months_ahead INTEGER DEFAULT 3)
RETURNS INTEGER
LANGUAGE plpgsql
AS $$
DECLARE
  base_month DATE := date_trunc('month', timezone('UTC', now()))::date;
  offset_idx INTEGER;
  from_date DATE;
  to_date DATE;
  part_name TEXT;
  created_count INTEGER := 0;
BEGIN
  IF months_back < 0 THEN
    months_back := 0;
  END IF;
  IF months_ahead < 0 THEN
    months_ahead := 0;
  END IF;

  FOR offset_idx IN (-months_back)..months_ahead LOOP
    from_date := (base_month + (offset_idx || ' month')::interval)::date;
    to_date := (from_date + interval '1 month')::date;
    part_name := format('sensor_datas_p%s', to_char(from_date, 'YYYYMM'));

    IF to_regclass('public.' || part_name) IS NULL THEN
      EXECUTE format(
        'CREATE TABLE %I PARTITION OF sensor_datas FOR VALUES FROM (%L) TO (%L)',
        part_name, from_date, to_date
      );
      created_count := created_count + 1;
    END IF;
  END LOOP;

  RETURN created_count;
END;
$$;

SELECT ensure_sensor_data_monthly_partitions(1, 3);

