-- [EN] Trigger function to auto-refresh patient.updated_at in UTC on UPDATE.
-- [ZH] 觸發器函式：在 UPDATE 時自動以 UTC 更新 patient.updated_at。
CREATE OR REPLACE FUNCTION set_utc_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at = timezone('UTC', now());
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_patient_set_updated_at ON patient;
CREATE TRIGGER trg_patient_set_updated_at
BEFORE UPDATE ON patient
FOR EACH ROW
EXECUTE FUNCTION set_utc_updated_at();
